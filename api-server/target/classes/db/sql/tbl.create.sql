CREATE SCHEMA NETTYBOOK_EXAMPLE_DB AUTHORIZATION DBA;
SET SCHEMA NETTYBOOK_EXAMPLE_DB;

DROP TABLE USERS IF EXISTS;

CREATE TABLE USERS (
	UIDX INT GENERATED BY DEFAULT AS IDENTITY (START WITH 1, INCREMENT BY 1) NOT NULL,
	USRID VARCHAR(45) NOT NULL,
	USRNAME VARCHAR(45) NOT NULL,
	UPWD VARCHAR(64) NOT NULL,
	PRIMARY KEY (UIDX)
);